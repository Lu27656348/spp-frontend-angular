export class FormularioExterno{
  constructor(
    nombres,
    apellidos,
    cedula,
    email,
    telefono,
    oficina,
    experiencia,
    habitacion,
    graduado,
    cargo
  ){
	  this.nombres = nombres;
	  this.apellidos = apellidos;
	  this.cedula = cedula;
	  this.email = email;
	  this.telefono = telefono;
	  this.oficina = oficina;
	  this.experiencia = experiencia;
	  this.habitacion = habitacion;
	  this.graduado = graduado;
	  this.cargo = cargo;
  }
}